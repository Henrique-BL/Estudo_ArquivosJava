    public void abrirTelaCadastroCliente() {
        TelaCadastroCliente janela = new TelaCadastroCliente();

        janela.controllerText = controllerClienteText;

        if (jRadioButtonBinario.isSelected()) {
            janela.controllerBin = controllerClienteBin;

        } else {
            janela.controllerText = controllerClienteText;

        }
        janela.setVisible(true);

    }

